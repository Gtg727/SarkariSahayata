function getSelectedLanguage() {
  return localStorage.getItem("selectedLanguage") || "en";
}

function setSelectedLanguage(lang) {
  localStorage.setItem("selectedLanguage", lang);
  applyTranslations(lang);
  updateLanguageButtons(lang);
  closeLanguageModal();
  showLanguageToast(languageNames[lang] || "English");

  window.dispatchEvent(new CustomEvent("languageChanged", {
    detail: { language: lang }
  }));
}

function applyTranslations(lang) {
  const current = translations[lang] || translations.en;
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (current[key]) {
      el.textContent = current[key];
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (current[key]) {
      el.setAttribute("placeholder", current[key]);
    }
  });

  document.querySelectorAll("[data-i18n-html]").forEach(el => {
    const key = el.getAttribute("data-i18n-html");
    if (current[key]) {
      el.innerHTML = current[key];
    }
  });

  updateDirection(lang);
}

function updateDirection(lang) {
  const rtlLanguages = ["ur"];
  if (rtlLanguages.includes(lang)) {
    document.documentElement.setAttribute("dir", "rtl");
    document.body.classList.add("rtl-layout");
  } else {
    document.documentElement.setAttribute("dir", "ltr");
    document.body.classList.remove("rtl-layout");
  }
}

function updateLanguageButtons(lang) {
  document.querySelectorAll(".lang-option").forEach(btn => {
    if (btn.dataset.lang === lang) {
      btn.classList.add("active");
    } else {
      btn.classList.remove("active");
    }
  });
}

function showLanguageToast(langName) {
  const oldToast = document.getElementById("languageToast");
  if (oldToast) oldToast.remove();

  const current = translations[getSelectedLanguage()] || translations.en;

  const el = document.createElement("div");
  el.id = "languageToast";
  el.className = "language-toast";
  el.innerText = `${current.language_set_to || "Language set to"} ${langName}`;
  document.body.appendChild(el);

  setTimeout(() => {
    el.classList.add("show");
  }, 50);

  setTimeout(() => {
    el.classList.remove("show");
    setTimeout(() => el.remove(), 300);
  }, 1800);
}

function openLanguageModal() {
  const modal = document.getElementById("langModalOverlay");
  if (modal) modal.classList.add("show");
}

function closeLanguageModal() {
  const modal = document.getElementById("langModalOverlay");
  if (modal) modal.classList.remove("show");
}

document.addEventListener("DOMContentLoaded", function () {
  const selectedLang = getSelectedLanguage();
  applyTranslations(selectedLang);
  updateLanguageButtons(selectedLang);

  const openBtn = document.getElementById("openLanguageModal");
  const closeBtn = document.getElementById("closeLanguageModal");
  const modal = document.getElementById("langModalOverlay");
  const langSearch = document.getElementById("languageSearch");

  if (openBtn) {
    openBtn.addEventListener("click", openLanguageModal);
  }

  if (closeBtn) {
    closeBtn.addEventListener("click", closeLanguageModal);
  }

  if (modal) {
    modal.addEventListener("click", function (e) {
      if (e.target === modal) {
        closeLanguageModal();
      }
    });
  }

  document.querySelectorAll(".lang-option").forEach(btn => {
    btn.addEventListener("click", function () {
      const lang = this.dataset.lang;
      setSelectedLanguage(lang);
    });
  });

  if (langSearch) {
    langSearch.addEventListener("input", function () {
      const query = this.value.toLowerCase().trim();

      document.querySelectorAll(".lang-option").forEach(btn => {
        const text = btn.textContent.toLowerCase();
        const code = btn.dataset.lang.toLowerCase();
        btn.style.display = (text.includes(query) || code.includes(query)) ? "flex" : "none";
      });
    });
  }
});