function translateSchemes(lang) {
  // Normalize lang code for schemeTranslations keys (zh-CN → zh_CN)
  const normLang = lang.replace("-", "_");
  const map = schemeTranslations[lang] || schemeTranslations[normLang];

  document.querySelectorAll(".scheme-category").forEach(el => {
    const original = el.getAttribute("data-category");
    if (!original) return;
    if (!map) {
      // For languages without a static map, restore to English
      el.textContent = original;
      return;
    }
    if (map[original]) {
      el.textContent = map[original];
    }
  });
}

/* ================================
   FREE TRANSLATION FOR SCHEME TITLES & DESCRIPTIONS
   Uses unofficial Google Translate endpoint — no API key needed
   Results are cached in sessionStorage per language
================================ */

async function translateText(text, targetLang) {
  if (!text || !targetLang || targetLang === "en") return text;

  // Check sessionStorage cache first
  const cacheKey = `tr_${targetLang}_${btoa(unescape(encodeURIComponent(text))).substring(0, 40)}`;
  try {
    const cached = sessionStorage.getItem(cacheKey);
    if (cached) return cached;
  } catch (e) { /* sessionStorage not available */ }

  try {
    // Use Flask proxy route to avoid CORS issues with Google Translate
    const res = await fetch(
      `/translate?tl=${encodeURIComponent(targetLang)}&q=${encodeURIComponent(text)}`
    );

    if (!res.ok) return text;

    const data = await res.json();
    const translated = data.t || text;

    // Cache the result
    try {
      sessionStorage.setItem(cacheKey, translated);
    } catch (e) { /* storage full, skip caching */ }

    return translated;
  } catch (e) {
    return text; // fallback to original on error
  }
}

async function translateSchemesContent(lang) {
  // If English selected, restore originals from data attributes
  if (lang === "en") {
    document.querySelectorAll(".scheme-title").forEach(el => {
      const original = el.getAttribute("data-title");
      if (original) el.textContent = original;
    });
    document.querySelectorAll(".scheme-desc").forEach(el => {
      const original = el.getAttribute("data-desc");
      if (original) el.textContent = original;
    });
    document.querySelectorAll(".scheme-category").forEach(el => {
      const original = el.getAttribute("data-category");
      if (original) el.textContent = original;
    });
    return;
  }

  const titles = document.querySelectorAll(".scheme-title");
  const descs = document.querySelectorAll(".scheme-desc");

  // Show loading indicator on scheme cards
  titles.forEach(el => {
    if (!el.getAttribute("data-title")) return;
    el.style.opacity = "0.5";
  });

  for (let el of titles) {
    const original = el.getAttribute("data-title");
    if (!original) continue;
    const translated = await translateText(original, lang);
    el.textContent = translated;
    el.style.opacity = "1";
  }

  for (let el of descs) {
    const original = el.getAttribute("data-desc");
    if (!original) continue;
    const translated = await translateText(original, lang);
    el.textContent = translated;
  }

  // Also translate categories that don't have a static map
  const normLang = lang.replace("-", "_");
  const map = schemeTranslations[lang] || schemeTranslations[normLang];
  if (!map) {
    for (let el of document.querySelectorAll(".scheme-category")) {
      const original = el.getAttribute("data-category");
      if (!original) continue;
      const translated = await translateText(original, lang);
      el.textContent = translated;
    }
  }
}